num1= int(input("Ingresar número 1: "))
num2= int(input("Ingresar número 2: "))
iguales= num1 == num2
print(f"Los números son iguales: {iguales}.")
diferentes= num1 != num2
print(f"Los números son diferentes: {diferentes}")
num1_mayor= num1 > num2
print(f"El primer número es mayor al segundo: {num1_mayor}")
num1_menor_igual= num1 <= num2
print(f"El primer número es menor o igual al segundo: {num1_menor_igual}")